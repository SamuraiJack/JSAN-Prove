package JSAN::Prove::App;

use Module::Pluggable sub_name => 'browsers', search_path => [ 'JSAN::Prove::App::Browser' ], instantiate => 'new';

 
#================================================================================================================================================================================================================================================
#================================================================================================================================================================================================================================================
sub start_browsers {
	my ($self, $url) = @_;
	
	my @browsers = grep { $_->is_startable } $self->browsers();
	
	foreach (@browsers) {
		die "Startable browser [$_] failed to start" unless $_->start;
	};
	
	return @browsers;
}


1;