#================================================================================================================================================================================================================================================
#================================================================================================================================================================================================================================================
sub start {
    my ($self, $url) = @_;
    
    $self->proc(Proc::Background->new($self->get_exe_path, $url));
}

 
#================================================================================================================================================================================================================================================
#================================================================================================================================================================================================================================================
sub stop {
    my $self = shift;
    
    $self->proc->die() if $self->proc;
}

1;