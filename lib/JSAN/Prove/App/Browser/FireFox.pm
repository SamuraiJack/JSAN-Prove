package JSAN::Prove::App::Browser::FireFox;

use Moose;
extends 'JSAN::Prove::App::Browser';

use JSAN::Prove::App::Process;



#================================================================================================================================================================================================================================================
sub get_exe_args {
	my ($self, $url) = @_;
	
	return ('C:\\Program Files\\Mozilla Firefox\\firefox.exe', $url) if $^O eq 'MSWin32';
	
	return ("firefox -new-window $url");
}


#================================================================================================================================================================================================================================================
sub is_startable {
	my ($self) = @_;
	
	return 1;
}


#================================================================================================================================================================================================================================================
sub kill_all_sibling {
	JSAN::Prove::App::Process->kill_by_name(qr/firefox/i);	
}


1;