package JSAN::Prove::App::Browser::IE;

use Moose;
extends 'JSAN::Prove::App::Browser';

use JSAN::Prove::App::Process;

use File::HomeDir;
use Path::Class;


#================================================================================================================================================================================================================================================
sub get_exe_args {
	my ($self, $url) = @_;
	
	my $file = file('c:', 'Program Files', 'Internet Explorer', 'iexplore.exe');
	
	return ($file . '', $url);
}


#================================================================================================================================================================================================================================================
sub is_startable {
	my ($self) = @_;
	
	return 1 if $^O eq 'MSWin32';
	
	return 0;
}


#================================================================================================================================================================================================================================================
sub kill_all_sibling {
	JSAN::Prove::App::Process->kill_by_name(qr/iexplore/i);	
}


1;